function menu_mouseover()
{
	document.getElementById('index_info').innerHTML='W tym miejscu znajduje się lista słodyczy, jakie można znaleźć w każdym zakątku świata! Kliknij, aby dowiedzieć się więcej!';
}
function menu_mouseout()
{
	document.getElementById('index_info').innerHTML='Tutaj będzie encyklopedia słodyczy. I będzie ona przepyszna!';
}